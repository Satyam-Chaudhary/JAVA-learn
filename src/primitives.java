public class primitives {
    //any datatype which can't be brpken further is primitive eg- int,character
    public static void main(String[] args) {
        int rollno = 69;
        char startname = 's';
        float marks = 98.668f;
        double largeDecimalNumbers = 1234567.765442;
        long largeInteger = 993477893949L;
        boolean check = true;//or false
        //String is not primitive
        String name = "Satyam Chaudhary";
        System.out.println(rollno);
        System.out.println(startname);
        System.out.println(marks);
        System.out.println(largeDecimalNumbers);
        System.out.println(largeInteger);
        System.out.println(check);
        System.out.println(name);

    }
}
